public class LoopStatememnts  {

      public static void main(String args[]){
          System.out.println("1.while statement");
          int a=10;
          while(a<20){
              System.out.println("print value " + a);
              a++;
          }

          System.out.println("2.do while statements");
          int num=10;
          do{
              System.out.println("P.V " + num);
              num++;
          }while(num<20);

          System.out.println("3.For Loop");

          for(int x=5; x<20; x++){
              System.out.println("Pv " +  x);
          }


      }
    }

